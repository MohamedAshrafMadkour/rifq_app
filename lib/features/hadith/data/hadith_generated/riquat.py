# نمط لاستخراج الرقم والنص داخل الأقواس
pattern = r"(\d+)- \(\((.*?)\)\)"

matches = re.findall(pattern, ruqya_text, flags=re.DOTALL)

result = []

for match in matches:
    number = match[0]
    text = match[1].strip()
    
    # إزالة أي أرقام داخل النص نفسه
    text = re.sub(r'\d+', '', text)
    
    result.append({
        "number": number,
        "text": text
    })

# حفظ الملف كـ JSON
